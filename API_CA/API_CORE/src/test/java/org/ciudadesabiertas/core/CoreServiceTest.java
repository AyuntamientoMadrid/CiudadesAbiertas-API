/**
 * @author Juan Carlos Ballesteros (Localidata)
 * @author Carlos Martínez de la Casa (Localidata)
 * @author Oscar Corcho (UPM, Localidata)
 *
 */
@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration(classes = { WebConfig.class })
@WebAppConfiguration
@FixMethodOrder(MethodSorters.NAME_ASCENDING)
public class CoreServiceTest {
	
	@Autowired
	private WebApplicationContext wac;

    @Autowired
    private CiudadService ciudadService;
    

    
    private MockMvc mockMvc;
    @Before
    public void setup() throws Exception {

    	
        this.mockMvc = MockMvcBuilders.webAppContextSetup(this.wac).build();
       
    }

    @Test
    public void test01_ciudadService() throws DAOException {
	
        final List<Ciudad> ciudad = ciudadService.findAll();
        
        assertTrue( ciudad.size()>0 );


    }
    
    
    @Test
    public void test02_ciudadController() {
        ServletContext servletContext = wac.getServletContext();
         
        Assert.assertNotNull(servletContext);
        Assert.assertTrue(servletContext instanceof MockServletContext);
        Assert.assertNotNull(wac.getBean("ciudadController"));
    }
    
    
    
    @Test    
    public void test03_List_ciudadController() throws Exception {
        this.mockMvc.perform(MockMvcRequestBuilders.get(CiudadController.CIUDAD_LIST+".json")).andExpect(MockMvcResultMatchers.status().isOk());
    }
    
    
   
    

}
